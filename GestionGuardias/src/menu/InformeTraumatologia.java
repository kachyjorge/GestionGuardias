    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed

        int cant = 0;
        try {
            String fechaD = jFechaD.getText();
            String fechaH = jFechaH.getText();
            Historia_Clinica hc = new Historia_Clinica();
            String Practica = "Traumatologia";
            cant = hc.cargarT_InformePrac(jTable, Practica, fechaD, fechaH);
            jTCantidad.setText(Integer.toString(cant));
            if(cant == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Para las fechas Ingresadas No hay Practicas Traumatologia", "Mensaje", WIDTH);
                jFechaD.setText("");
                jFechaD.requestFocus();
                jFechaH.setText("");
                jTCantidad.setText("");
            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(InformeTraumatologia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBtnBuscarActionPerformed

