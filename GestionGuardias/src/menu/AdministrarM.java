private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed

    boolean bandera = false;
    Medicamento miP = new Medicamento();
    int codigo = 0;
    if (jInternacion.isSelected()) 
    {        
        codigo = Integer.parseInt(this.jTCod_m.getText());
        this.jTCod_m.setText("");
        this.jTCod_m.requestFocus();
        try {
            bandera = miP.BuscarCod_M(codigo);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministrarM.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!bandera) {
            JOptionPane.showMessageDialog(rootPane, "El Medicamento No esta Disponible", "Medicaci贸n", WIDTH);

        } else {
            try {
                JOptionPane.showMessageDialog(rootPane, "El Medicamento esta Disponible", "Medicaci贸n", WIDTH);
                AdministarMedicamentoInternacion amI = new AdministarMedicamentoInternacion(codigo);
                amI.setVisible(true);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdministrarM.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    } else {
        if (jAmbulatorio.isSelected()) 
        {
            codigo = Integer.parseInt(this.jTCod_m.getText());
        this.jTCod_m.setText("");
        this.jTCod_m.requestFocus();
        try {
            bandera = miP.BuscarCod_M(codigo);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministrarM.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!bandera) {
            JOptionPane.showMessageDialog(rootPane, "El Medicamento No esta Disponible", "Medicaci贸n", WIDTH);

        } else {
            try {
                JOptionPane.showMessageDialog(rootPane, "El Medicamento esta Disponible", "Medicaci贸n", WIDTH);
                AdministrarMedicamento am = new AdministrarMedicamento(codigo);
                am.setVisible(true);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdministrarM.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        }
    }


}//GEN-LAST:event_jBtnBuscarActionPerformed

