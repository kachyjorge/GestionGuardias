    public IngresarPacienteInternación(String idcama) throws ClassNotFoundException {
        initComponents();
        id = idcama;
        try {
            Connection cn = Conexion.Cadena();
        
            sentencia = cn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            rsDatos = sentencia.executeQuery("select historia_clinica.nro_consulta, pacientes.nombre_pac, pacientes.apellido_pac from historia_clinica inner join pacientes on historia_clinica.dni_p = pacientes.dni_p;");
            
            while (rsDatos.next()) 
            {
                int nro = rsDatos.getInt("nro_consulta");
                String nombre = rsDatos.getString("nombre_pac");
                String apellido = rsDatos.getString("apellido_pac");
                String elemento = nro + " - " + apellido + ", " + nombre;
                this.jCBPaciente.addItem(elemento);
                            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(IngresarPacienteInternación.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }

