private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed

        try {
            Internacion inter = new Internacion();
            String elemento;
            elemento = (String) jCBPaciente.getSelectedItem().toString();
            int x = elemento.indexOf(" - ");
            String nro = elemento.substring(0, x);
            
            inter.setCod_int(inter.buscarUltimo());
            inter.setFecha_ing(dateIngreso.getText());
            //inter.setFecha_alta(dateAlta.getText());
            //inter.setMotivo(jTMotivo.getText());
            inter.setIdcamas(Integer.parseInt(id));
            
            inter.setNro_consulta(Integer.parseInt(nro));
            
            inter.InsertarInternacion();
            
            JOptionPane.showMessageDialog(rootPane, "Los Datos Ingresados se Guardaron CORRECTAMENTE","Guardado", WIDTH);
            
            this.dispose();
            
        } catch (SQLException ex) {
            Logger.getLogger(IngresarPacienteInternación.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_jBtnGuardarActionPerformed

