    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
   
        try {
            Medicos me = new Medicos();
            me.setCod_medico(Integer.parseInt(jTC_Med.getText()));
            me.setEsp_medica(jTEspecialidad.getText());
            me.setCargo_med(jCBCargo.getSelectedItem().toString());
            me.setNombre_med(jTNombre.getText());
            me.setApellido_med(jTApellido.getText());
            me.setDireccion_med(jTDireccion.getText());
            
                    
            me.InsertarMedico();
            
            JOptionPane.showMessageDialog(rootPane, "El Registro del Medico se Genero CORRECTAMENTE", "Guardado", WIDTH);
        } catch (SQLException ex) {
            Logger.getLogger(CargarMedico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jBtnGuardarActionPerformed

